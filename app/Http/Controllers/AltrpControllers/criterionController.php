    /**
     * criterionController constructor.
     */
    public function __construct()
    {
        $this->modelClass = criterion::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(criterionStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $criterion = new criterion;

        $regChat = ChatService::registerInChat($criterion->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($criterion->getTable(), $data);

        $criterion->fill($data);

        $res = $criterion->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$criterion], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $criterion = criterion::find($id);

        if(! $criterion) {
            return response()->json(trans("responses.not_found.criterion"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'criterion')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $criterion = $criterion->load($relations);
        }

        $criterion = $this->getRemoteData($model, $criterion, true);

        return response()->json($criterion, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(criterionUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $criterion = criterion::find($id);

        if(! $criterion) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.criterion")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($criterion->getTable(), $data);

        $result = $criterion->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $criterion = criterion::find($id);

        if(! $criterion) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.criterion")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $criterion->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.criterion")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $criterion = criterion::find($id);

        if(! $criterion) {
            return response()->json(['success'=>false, 'message' =>"criterion not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($criterion->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $criterion->$column = $request->column_value;

        if ($criterion->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $criteria = criterion::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($criteria, 200, [], JSON_UNESCAPED_UNICODE);
    }

