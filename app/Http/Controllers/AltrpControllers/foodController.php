    /**
     * foodController constructor.
     */
    public function __construct()
    {
        $this->modelClass = food::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(foodStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $food = new food;

        $regChat = ChatService::registerInChat($food->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($food->getTable(), $data);

        $food->fill($data);

        $res = $food->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$food], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $food = food::find($id);

        if(! $food) {
            return response()->json(trans("responses.not_found.food"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'food')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $food = $food->load($relations);
        }

        $food = $this->getRemoteData($model, $food, true);

        return response()->json($food, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(foodUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $food = food::find($id);

        if(! $food) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.food")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($food->getTable(), $data);

        $result = $food->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $food = food::find($id);

        if(! $food) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.food")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $food->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.food")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $food = food::find($id);

        if(! $food) {
            return response()->json(['success'=>false, 'message' =>"food not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($food->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $food->$column = $request->column_value;

        if ($food->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $food = food::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($food, 200, [], JSON_UNESCAPED_UNICODE);
    }

