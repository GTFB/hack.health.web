    /**
     * trainingController constructor.
     */
    public function __construct()
    {
        $this->modelClass = training::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(trainingStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $training = new training;

        $regChat = ChatService::registerInChat($training->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($training->getTable(), $data);

        $training->fill($data);

        $res = $training->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$training], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $training = training::find($id);

        if(! $training) {
            return response()->json(trans("responses.not_found.training"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'training')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $training = $training->load($relations);
        }

        $training = $this->getRemoteData($model, $training, true);

        return response()->json($training, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(trainingUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $training = training::find($id);

        if(! $training) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.training")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($training->getTable(), $data);

        $result = $training->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $training = training::find($id);

        if(! $training) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.training")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $training->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.training")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $training = training::find($id);

        if(! $training) {
            return response()->json(['success'=>false, 'message' =>"training not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($training->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $training->$column = $request->column_value;

        if ($training->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $trainings = training::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($trainings, 200, [], JSON_UNESCAPED_UNICODE);
    }

